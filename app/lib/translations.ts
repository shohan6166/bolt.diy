// Translation utility functions
export const useTranslation = (language: 'en' | 'bn' = 'bn') => {
  const t = (key: keyof typeof translations): string => {
    return translations[key]?.[language] || key;
  };

  const formatNumber = (num: number): string => {
    if (language === 'bn') {
      return num.toString().replace(/[0-9]/g, (digit) => {
        const bengaliDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
        return bengaliDigits[parseInt(digit)];
      });
    }
    return num.toString();
  };

  const formatCurrency = (amount: number): string => {
    const formattedAmount = new Intl.NumberFormat('en-BD', {
      style: 'currency',
      currency: 'BDT',
      minimumFractionDigits: 0
    }).format(amount);
    
    if (language === 'bn') {
      return formattedAmount.replace(/[0-9]/g, (digit) => {
        const bengaliDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
        return bengaliDigits[parseInt(digit)];
      });
    }
    
    return formattedAmount;
  };

  const formatDate = (date: Date | string): string => {
    const dateObj = typeof date === 'string' ? new Date(date) : date;
    
    if (language === 'bn') {
      const options: Intl.DateTimeFormatOptions = {
        year: 'numeric',
        month: 'long',
        day: 'numeric'
      };
      
      let formatted = dateObj.toLocaleDateString('bn-BD', options);
      
      // Replace English month names with Bengali if needed
      const monthMap = {
        'January': 'জানুয়ারি',
        'February': 'ফেব্রুয়ারি',
        'March': 'মার্চ',
        'April': 'এপ্রিল',
        'May': 'মে',
        'June': 'জুন',
        'July': 'জুলাই',
        'August': 'আগস্ট',
        'September': 'সেপ্টেম্বর',
        'October': 'অক্টোবর',
        'November': 'নভেম্বর',
        'December': 'ডিসেম্বর'
      };
      
      Object.entries(monthMap).forEach(([eng, ben]) => {
        formatted = formatted.replace(eng, ben);
      });
      
      return formatted;
    }
    
    return dateObj.toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    });
  };

  return {
    t,
    formatNumber,
    formatCurrency,
    formatDate,
    language
  };
};

